package powercraft.api.world;

/**
 * @author James
 * Locations for use in the API
 */
public class Location {
	@SuppressWarnings("javadoc")
	public int x;
	@SuppressWarnings("javadoc")
	public int y;
	@SuppressWarnings("javadoc")
	public int z;
	
	@SuppressWarnings("javadoc")
	public int getX() {
		return this.x;
	}
	
	@SuppressWarnings("javadoc")
	public void setX(int x) {
		this.x = x;
	}
	
	@SuppressWarnings("javadoc")
	public int getY() {
		return this.y;
	}
	
	@SuppressWarnings("javadoc")
	public void setY(int y) {
		this.y = y;
	}
	
	@SuppressWarnings("javadoc")
	public int getZ() {
		return this.z;
	}
	
	@SuppressWarnings("javadoc")
	public void setZ(int z) {
		this.z = z;
	}
}
